/** The scariest functions. sin, cos, etc. are provided using the built-ins in real_functions.js. */

/**
 * Returns x + y.
 * @param x {number}
 * @param y {number}
 * @returns {number}
 */
export function add (x, y) {
  return x + y
}

/**
 * Returns x - y.
 * @param x {number}
 * @param y {number}
 * @returns {number}
 */
export function subtract (x, y) {
  return x - y
}

/**
 * Returns x * y.
 * @param x {number}
 * @param y {number}
 * @returns {number}
 */
export function multiply (x, y) {
  return x * y
}

/**
 * Returns x / y.
 * @param x {number}
 * @param y {number}
 * @returns {number}
 */
export function divide (x, y) {
  return x / y
}
